#pragma once
#include <string>


/** Максимально допустимая длина слова — защита от вырожденных случаев. */
static constexpr int GMaxWordLength = 8;


/**
 * Возвращает true, если каждый символ строки Word является кириллической
 * или латинской буквой (в верхнем или нижнем регистре).
 * Пустая строка считается недопустимой.
 *
 * @param Word  Широкосимвольная строка, введённая пользователем.
 * @return      true, если слово прошло валидацию.
 */
bool IsValidWord(const std::wstring& Word);


/**
 * Многократно перемешивает Word случайным образом до тех пор, пока
 * результат не совпадёт с оригиналом. Каждую промежуточную перестановку
 * выводит в stdout вместе с порядковым номером попытки.
 *
 * @param OriginalWord  Проверенное исходное слово для перемешивания.
 * @return              Общее количество выполненных попыток перемешивания
 *                      (последняя попытка даёт исходное слово).
 */
int ShuffleUntilMatch(const std::wstring& OriginalWord);